package Ex_04_Operators_Part_4;

public class Operators_Arithmetic_Operators {
    public static void main(String[] args) {

        // Arithmetic Operators
        //        + (Addition)
        //       - (Subtraction)
        //       * (Multiplication)
        //       / (Division)
        //       % (Modulus) | Modulus - Remainder
        int a = 20;
        int b = 3;
        float c = 3.0f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        System.out.println(a/c); // answer will come in decimal as float is declare for this
        System.out.println("a+b");// in "" it will consider as string and not as Int
        System.out.println(a+b);

    }
}
