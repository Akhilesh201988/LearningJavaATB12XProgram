package Ex_03_Literals_Part_3;

public class Types_of_Literals {

        public static void main(String[] args) {

            /**
             * ### **Types of Literals**
             * 1. Boolean Literals - true, false
             * 2. Character Literals
             * 3. String Literals
             * 4. Integral Literals
             * 5. Floating Literals
             * 6. null Literal , **Scientific** Literals
             */
            int i=65;
            // Literals= Integral

            final float f=3.14f;
            // Literals= float

            final int girl_age=18;
            // Literals= final Integral

        }
    }

