package Ex_04_Operators;

public class Relational_Operators1 {
    public static void main(String[] args) {
        // < Less Than
        // < =  -> Less than or equal to
        // > Greater
        // > = Greater or equal
        // == ->  Equal to (but checking)
        // != -> Not equal
        // All of them will result boolean output.

// Written by Akhilesh
        int i=10;
        int j=20;
        System.out.println(i>j);
        System.out.println(j<i);
        System.out.println(i>=j);
        System.out.println(j<=i);
        System.out.println(i==j);
        System.out.println(j!=i);

        //        ✔️ Pros:
//        Short and simple
//
//        Good for quick checks or debugging
//
//❌ Cons:
//        Hard to reuse the result later in the code
//
//        Meaning of the expression might not be obvious to others reading the code





    }
}

