package Ex_02_Java_Basics_Part_2;

public class Variable_Numeric_data_types {
    public static void main(String[] args) {

        byte b = 10; // byte b=-128 to 127
        int i = 10;
        Short s = 10;
        char c = 'A';
        long l = 9773747807l; // long datatype always end with small l or capital L
        long ll = 9773747807L; // long datatype always end with small l or capital L
        float f= 3.14f; // long datatype always end with small l or capital f
        float ff=3.14F; // long datatype always end with small l or capital F
        double d=3.14515151515615;
        System.out.println(d);
    }
}